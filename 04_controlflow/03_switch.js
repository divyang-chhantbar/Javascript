const month = "april"

switch (month) {
    case "january":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "mar":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}